    public function saveCart(array $cart)
    {
        $saved_cart = session()->get('saved_cart');

        foreach ($cart as $key => $value) {
            if (!isset($saved_cart[$value->id])) {
                $saved_cart[$value->id] = [
                    "idProduk" => $value->id,
                    "name" => $value->name,
                    "quantity" => $value->quantity,
                    "price" => $value->price,
                    "image" => $value->image_url,
                    "stock" => $value->stock
                ];
            }
        }
    }
